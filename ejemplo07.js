let opcion = parseInt(prompt('Ingrese un numero'));

switch (opcion) {
    case 1:
        console.log('Se ejecuto caso 1');
        break
    case 2:
        console.log('Se ejecuto caso 2');
        break
    case 3:
        console.log('Se ejecuto caso 3');
        break
    default:
        console.log('No se ejecuto nada');
}